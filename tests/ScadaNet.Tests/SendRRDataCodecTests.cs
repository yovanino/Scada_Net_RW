using ScadaNet.EtherNetIp;

namespace ScadaNet.Tests;

public class SendRRDataCodecTests
{
    [Fact]
    public void EncodeRequest_wraps_unconnected_data_item()
    {
        var encoded = SendRRDataCodec.EncodeRequest(
            0x12345678,
            new SendRRDataRequest([0x4C, 0x00]));

        Assert.Equal(
            [
                0x6F, 0x00,
                0x12, 0x00,
                0x78, 0x56, 0x34, 0x12,
                0x00, 0x00, 0x00, 0x00,
                0x00, 0x00, 0x00, 0x00,
                0x00, 0x00, 0x00, 0x00,
                0x00, 0x00, 0x00, 0x00,
                0x00, 0x00, 0x00, 0x00,
                0x00, 0x00,
                0x02, 0x00,
                0x00, 0x00,
                0x00, 0x00,
                0xB2, 0x00,
                0x02, 0x00,
                0x4C, 0x00
            ],
            encoded);
    }

    [Fact]
    public void DecodeResponse_returns_unconnected_data_item()
    {
        byte[] response =
        [
            0x6F, 0x00,
            0x12, 0x00,
            0x78, 0x56, 0x34, 0x12,
            0x00, 0x00, 0x00, 0x00,
            0x00, 0x00, 0x00, 0x00,
            0x00, 0x00, 0x00, 0x00,
            0x00, 0x00, 0x00, 0x00,
            0x00, 0x00, 0x00, 0x00,
            0x00, 0x00,
            0x02, 0x00,
            0x00, 0x00,
            0x00, 0x00,
            0xB2, 0x00,
            0x02, 0x00,
            0xCC, 0x00
        ];

        var decoded = SendRRDataCodec.DecodeResponse(response);

        Assert.Equal([0xCC, 0x00], decoded.Data);
    }
}
